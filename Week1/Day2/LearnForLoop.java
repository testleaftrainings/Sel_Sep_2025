package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {

		// print numbers from 1 to 5
		// starting number=1
		// final number=5
		// increment=1

		// 1 2 3 4 5
		for (int i = 0; i <= 25; i++) {
			System.out.println(i);
		}

		// Reverse the numbers from 1 to 5
		// 5 to 1
		// 5 4 3 2 1
		// starting number=5
		// final number=1
		// decrement=1

		// 5 4 3 2 1 0 -1 -2
		for (int i = 5; i >= 1; i--) {
			System.out.print(i);
		}

		//

	}

}
