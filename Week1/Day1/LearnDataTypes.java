package week1.day1;

public class LearnDataTypes {

	public static void main(String[] args) {

		// byte
		byte age = 30;

		// short
		short ageOfPerson = 130;

		short amount = 30000;

		// int
		int depositAmount = 50000;

		// long
		long mobileNumber = 8925411170L;

		// float
		float rateOfInterest = 7.25f;

		// float and double
		float rupee = 1.123456789f;

		System.out.println(rupee);

		double dollar = 1.123456789;

		System.out.println(dollar);

		// char
		char logoOfTestleaf = 'T';

		// boolean
		boolean hadBreakfast = false;

		// String
		String name = "Vineeth";

		int ageOfVineeth = 30;

		// Print multiple variables
		System.out.println(name + " " + ageOfVineeth);

		// Print Age is 30

		System.out.println("Age is " + ageOfVineeth);

	}

}
